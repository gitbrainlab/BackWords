import SwiftUI

/// Spacing constants derived from an 8-point grid.
enum Spacing {
    /// 4 pt — icon padding, tight gaps.
    static let xs: CGFloat = 4
    /// 8 pt — inline spacing, tag gaps.
    static let sm: CGFloat = 8
    /// 12 pt — text-to-icon, compact cells.
    static let md: CGFloat = 12
    /// 16 pt — standard padding, card insets.
    static let lg: CGFloat = 16
    /// 24 pt — section separation.
    static let xl: CGFloat = 24
    /// 32 pt — major section breaks.
    static let xxl: CGFloat = 32
    /// 48 pt — screen-level top/bottom padding.
    static let xxxl: CGFloat = 48
}
